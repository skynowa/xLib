//-------------------------------------------------------------------------------------------------
template<typename T>
T &
operator ++ (T &a_value)
{
    return a_value = static_cast<T>(a_value + 1);
}
//-------------------------------------------------------------------------------------------------
template<typename T>
T &
operator -- (T &a_value)
{
    return a_value = static_cast<T>(a_value - 1);
}
